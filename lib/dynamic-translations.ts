export function getTranslatedPackageSubtitle(
  item: { slug?: string | null; subtitle_en?: string; subtitle_es?: string } | null | undefined,
  defaultSub: string,
  lang: string
) {
  if (lang === 'pt') return defaultSub;

  // 1. Prioridade: Tradução específica vinda do Banco de Dados
  if (item) {
    if (lang === 'en' && item.subtitle_en) return item.subtitle_en;
    if (lang === 'es' && item.subtitle_es) return item.subtitle_es;
  }
  
  // 2. Fallback: Mapa estático legado
  if (item?.slug) {
    const translations = dynamicPackageSubtitles[item.slug];
    if (translations && translations[lang]) {
      return translations[lang];
    }
  }
  
  return defaultSub;
}

